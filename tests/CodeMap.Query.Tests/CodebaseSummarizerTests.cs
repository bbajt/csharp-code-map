        var diSection = result.Sections.FirstOrDefault(s =>
            s.Title.Contains("Dependency Injection", StringComparison.OrdinalIgnoreCase));

        diSection.Should().NotBeNull();
        diSection!.Truncated.Should().BeFalse();
        diSection.TotalAvailable.Should().Be(2);
        diSection.Content.Should().NotContain("Showing");
    }

    [Fact]
    public async Task SummarizeAsync_AllSections_TruncationApplied()
    {
        // 3 items per kind, cap = 1 → all non-empty sections should be truncated
        SetupFacts(FactKind.DiRegistration, "IFoo → Foo|Singleton", "IBar → Bar|Scoped", "IBaz → Baz|Transient");
        SetupFacts(FactKind.Log, "msg1|Information", "msg2|Debug", "msg3|Warning");
        foreach (FactKind kind in Enum.GetValues<FactKind>()
            .Where(k => k != FactKind.DiRegistration && k != FactKind.Log))
            SetupNoFacts(kind);
        SetupProjectDiagnostics();
        SetupSemanticLevel(SemanticLevel.Full);

        var result = await CodebaseSummarizer.SummarizeAsync(
            _store, Repo, Sha, "TestSolution",
            sectionFilter: null, maxItemsPerSection: 1, CancellationToken.None);

        var dataSections = result.Sections.Where(s =>
            s.Title.Contains("Dependency Injection", StringComparison.OrdinalIgnoreCase) ||
            s.Title.Contains("Logging", StringComparison.OrdinalIgnoreCase)).ToList();

        dataSections.Should().HaveCount(2);
        dataSections.Should().AllSatisfy(s => s.Truncated.Should().BeTrue());
        dataSections.Should().AllSatisfy(s => s.ItemCount.Should().Be(1));
    }
}
