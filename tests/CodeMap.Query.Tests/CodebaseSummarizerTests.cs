    [Fact]
    public async Task SummarizeAsync_MaxItemsPerSection_IsCapped()
    {
        SetupAllFactsEmpty();
        SetupProjectDiagnostics();
        SetupSemanticLevel(SemanticLevel.Full);

        // maxItemsPerSection=0 should be clamped to 1
        var result = await CodebaseSummarizer.SummarizeAsync(
            _store, Repo, Sha, "TestSolution", null, maxItemsPerSection: 0, CancellationToken.None);

        // Verify the store was called with limit >= 1 (not 0)
        await _store.Received().GetFactsByKindAsync(
            Repo, Sha, Arg.Any<FactKind>(), Arg.Is<int>(n => n >= 1), Arg.Any<CancellationToken>());
    }

    [Fact]
    public async Task SummarizeAsync_WithAllFacts_IncludesAllSections()
    {
