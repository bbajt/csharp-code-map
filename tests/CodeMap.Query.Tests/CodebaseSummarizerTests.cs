    [Fact]
    public async Task SummarizeAsync_MixedFilter_ShowsDataAndPlaceholders()
    {
        // Has exceptions but no resilience facts
        SetupFacts(FactKind.Exception, "InvalidOperationException|throw new");
        SetupNoFacts(FactKind.RetryPolicy);
        SetupNoFacts(FactKind.Route);
        SetupNoFacts(FactKind.Config);
        SetupNoFacts(FactKind.DbTable);
        SetupNoFacts(FactKind.DiRegistration);
        SetupNoFacts(FactKind.Middleware);
        SetupNoFacts(FactKind.Log);
        SetupProjectDiagnostics();
        SetupSemanticLevel(SemanticLevel.Full);

        var result = await CodebaseSummarizer.SummarizeAsync(
            _store, Repo, Sha, "TestSolution",
            sectionFilter: ["exceptions", "resilience"], 50, CancellationToken.None);

        var exSection = result.Sections.FirstOrDefault(s =>
            s.Title.Contains("Error Handling", StringComparison.OrdinalIgnoreCase));
        var resSection = result.Sections.FirstOrDefault(s =>
            s.Title.Contains("Resilience", StringComparison.OrdinalIgnoreCase));

        exSection.Should().NotBeNull("exceptions section has data");
        exSection!.ItemCount.Should().Be(1);

        resSection.Should().NotBeNull("resilience was explicitly requested");
        resSection!.ItemCount.Should().Be(0);
        resSection.Content.Should().Contain("No retry or circuit-breaker policies detected");
    }

    [Fact]
    public async Task SummarizeAsync_Section_Truncated_ShowsIndicator()
    {
        // 5 DI facts, cap = 2 → should detect truncation
        SetupFacts(FactKind.DiRegistration,
            "IFoo → Foo|Singleton",
            "IBar → Bar|Scoped",
            "IBaz → Baz|Transient",
            "IQux → Qux|Singleton",
            "IFive → Five|Singleton");
        foreach (FactKind kind in Enum.GetValues<FactKind>().Where(k => k != FactKind.DiRegistration))
            SetupNoFacts(kind);
        SetupProjectDiagnostics();
        SetupSemanticLevel(SemanticLevel.Full);

        var result = await CodebaseSummarizer.SummarizeAsync(
            _store, Repo, Sha, "TestSolution",
            sectionFilter: ["di"], maxItemsPerSection: 2, CancellationToken.None);

        var diSection = result.Sections.FirstOrDefault(s =>
            s.Title.Contains("Dependency Injection", StringComparison.OrdinalIgnoreCase));

        diSection.Should().NotBeNull();
        diSection!.Truncated.Should().BeTrue("cap=2, 5 items exist");
        diSection.ItemCount.Should().Be(2);
        diSection.TotalAvailable.Should().BeNull("exact total unknown with +1 probe");
        diSection.Content.Should().Contain("Showing 2 of 2+");
        diSection.Content.Should().Contain("max_items_per_section");
    }

    [Fact]
    public async Task SummarizeAsync_Section_NotTruncated_NoIndicator()
    {
        SetupFacts(FactKind.DiRegistration,
            "IFoo → Foo|Singleton",
            "IBar → Bar|Scoped");
        foreach (FactKind kind in Enum.GetValues<FactKind>().Where(k => k != FactKind.DiRegistration))
            SetupNoFacts(kind);
        SetupProjectDiagnostics();
        SetupSemanticLevel(SemanticLevel.Full);

        var result = await CodebaseSummarizer.SummarizeAsync(
            _store, Repo, Sha, "TestSolution",
