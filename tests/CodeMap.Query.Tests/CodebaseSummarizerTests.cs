namespace CodeMap.Query.Tests;

using CodeMap.Core.Enums;
using CodeMap.Core.Interfaces;
using CodeMap.Core.Models;
using CodeMap.Core.Types;
using FluentAssertions;
using NSubstitute;

public sealed class CodebaseSummarizerTests
{
    private static readonly RepoId Repo = RepoId.From("summarizer-repo");
    private static readonly CommitSha Sha = CommitSha.From(new string('b', 40));

    private readonly ISymbolStore _store = Substitute.For<ISymbolStore>();

    private void SetupFacts(FactKind kind, params string[] values)
    {
        var facts = values.Select((v, i) => new StoredFact(
            SymbolId: SymbolId.From($"M:Test.Method{i}"),
            StableId: null,
            Kind: kind,
            Value: v,
            FilePath: FilePath.From("Test.cs"),
            LineStart: i + 1,
            LineEnd: i + 1,
            Confidence: Confidence.High)).ToList<StoredFact>();

        _store.GetFactsByKindAsync(Repo, Sha, kind, Arg.Any<int>(), Arg.Any<CancellationToken>())
              .Returns(facts);
    }

    private void SetupNoFacts(FactKind kind)
        => _store.GetFactsByKindAsync(Repo, Sha, kind, Arg.Any<int>(), Arg.Any<CancellationToken>())
                 .Returns(Array.Empty<StoredFact>());

    private void SetupAllFactsEmpty()
    {
        foreach (FactKind kind in Enum.GetValues<FactKind>())
            SetupNoFacts(kind);
    }

    private void SetupProjectDiagnostics(params ProjectDiagnostic[] diags)
        => _store.GetProjectDiagnosticsAsync(Repo, Sha, Arg.Any<CancellationToken>())
                 .Returns((IReadOnlyList<ProjectDiagnostic>)diags.ToList());

    private void SetupSemanticLevel(SemanticLevel level)
        => _store.GetSemanticLevelAsync(Repo, Sha, Arg.Any<CancellationToken>())
                 .Returns((SemanticLevel?)level);

    [Fact]
    public async Task SummarizeAsync_NoFacts_ReturnsOverviewAndMetricsSectionsOnly()
    {
        SetupAllFactsEmpty();
        SetupProjectDiagnostics();
        SetupSemanticLevel(SemanticLevel.Full);

        var result = await CodebaseSummarizer.SummarizeAsync(
            _store, Repo, Sha, "TestSolution", null, 50, CancellationToken.None);

        result.SolutionName.Should().Be("TestSolution");
        result.Sections.Should().HaveCount(2); // overview + metrics (both always included)
        result.Markdown.Should().Contain("# TestSolution — Codebase Summary");
        result.Markdown.Should().Contain("*Generated by CodeMap MCP*");
    }

    [Fact]
    public async Task SummarizeAsync_WithRouteFacts_IncludesApiSection()
    {
        SetupAllFactsEmpty();
        SetupFacts(FactKind.Route, "GET /api/orders", "POST /api/orders");
        SetupProjectDiagnostics();
        SetupSemanticLevel(SemanticLevel.Full);

        var result = await CodebaseSummarizer.SummarizeAsync(
            _store, Repo, Sha, "TestSolution", null, 50, CancellationToken.None);

        var apiSection = result.Sections.FirstOrDefault(s => s.Title.Contains("API Surface"));
        apiSection.Should().NotBeNull();
        apiSection!.ItemCount.Should().Be(2);
        result.Stats.EndpointCount.Should().Be(2);
    }

    [Fact]
    public async Task SummarizeAsync_WithConfigFacts_GroupsByTopLevelPrefix()
